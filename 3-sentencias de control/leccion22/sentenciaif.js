//Aqui veremos que son las sentencias de control if - else en JavaScript

let ejex = 3, ejey = 2;
/**
 * La sentencia if nos permite ejecutar un bloque de 
 * codigo si la condicion dentro del parantesis sea verdadera.
 * Si tambien tenemos un else este se ejecutara cuando la 
 * afirmacion sea falsa.
 */
if (ejex > ejey) {
    
    console.log("Condicion: " + (ejex > ejey));

} else {
    
    console.log("Condicion: " + (ejex < ejey));

}