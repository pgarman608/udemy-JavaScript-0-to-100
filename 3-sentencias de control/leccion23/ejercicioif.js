// Aqui veremos mas informacion sobre los if-else en JavaScript

let numero = 2;

/**
 * Podemos poner en la sentencia else otro if para poder comprobar 
 * otra condicion en la misma structura.
 */

if (numero == 1) {
    
    console.log("Numero uno");

} else if (numero == 2) {
    
    console.log("Numero dos");

} else if (numero == 3) {
    
    console.log("Numero tres");

} else if (numero == 4) {
    
    console.log("Numero cuatro");

} else {
    
    console.log("Numero Desconocido");

}
