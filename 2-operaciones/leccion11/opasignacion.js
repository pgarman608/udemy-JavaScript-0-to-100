//Aqui veremos lo operadores de asginacion que hay en JavaScript

let asignacion = 5;

asignacion += 5;

console.log(asignacion);

/**
 * Los operadores de asignacion son aquellos simbolos que se 
 * utilizan para guardar un dato en una variable.
 * El igual solo ya seria un operador de asignacion pero se le 
 * pueden escribir otros simbolos detras para que haga mas acciones.
 * += 5 (variable = variable mas 5)
 * -= 5 (variable = variable menos 5)
 * *= 5 (variable = variable por 5)
 * /= 5 (variable = variable entre 5 y recoger el coeficiente)
 * %= 5 (variable = variable entre 5 y recoger el resto)
 * **= 5 (variable = variable elevado a 5)
 */