//Aqui veremos las operaciones relacionales que se usan en JavaScript
let ejex = 5, ejey = 15;

console.log("Mayor: " + (ejex > ejey));

console.log("Menor: " + (ejex < ejey));

console.log("Mayor o igual: " + (ejex >= ejey));

console.log("Menor o igual: " + (ejex <= ejey));

/**
 * Las operaciones relacionales son las 4 siguientes:
 * 
 * 1. Mayor:
 * Con el simbolo ">" nos dice si el dato de la izquierda es 
 * mas grande que la derecha o no lo es.
 * 
 * 2. Menor:
 * Con el simbolo "<" nos dice si el dato de la derecha es 
 * mas grande que la izquierda o no lo es.
 * 
 * 3. Mayor o igual:
 * Con el simbolo ">=" nos dice si el dato de la izquierda es 
 * mas grande o igual que la derecha o no lo es.
 * 
 * 4. Menor o igual:
 * Con el simbolo "<=" nos dice si el dato de la derecha es 
 * mas grande o igual que la izquierda o no lo es.
 */