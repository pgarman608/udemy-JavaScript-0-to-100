/**
 * Aqui veremos que es y para que se utiliza el operador ternario en JavaScript
 * 
 * La idea del operador ternario es compactar al sentencia de if en menos espacio.
 * La sintaxis es la siguiente: condicion ? datoTrue : datofalse
 * Si la condicion es correcta se aparecera el primer dato y si es falsa aparece el segundo dato
 */

let resultado = (1 == 2) ? true : false;

console.log(resultado);
