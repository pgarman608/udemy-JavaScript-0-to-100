// Aqui vamos a ver como se realizan todas las operaciones matematicas simples en JavaScript

let numero1 = 10, numero2 = 5;

/**
 * 1. Suma
 * 
 * Se usa el simbolo del "+".
 */

console.log("Suma: " + (numero1 + numero2));

/**
 * 2. Resta
 * 
 * Se usa el simbolo del "-".
 */

console.log("Resta: " + (numero1 - numero2));

/**
 * 3. Multiplicacion
 * 
 * Se usa el simbolo del "-".
 */

console.log("Multiplicacion: " + (numero1 * numero2));

/**
 * 4. Divisi√≥n
 * 
 * Se usa el simbolo del "/" para obtener el coeficiente.
 * Se usa el simbolo del "%" para obtener el resto.
 */

console.log("Division (coeficiente): " + (numero1 / numero2));

console.log("Division (Resto): " + (numero1 % numero2));

/**
 * 5. Potencia
 * 
 * Se usa dos simbolos de multiplicacion para realizar la potencia.
 */

console.log("Potencia: " + (numero1 ** numero2));
