// Aqui veremos como podemos declarar funciones flecha en JavaScript

/**
 * Las funciones flecha nos permite crear funciones dentro de
 * otro codigo ocupando menos espacio.
 * @param {*} a 
 * @param {*} b 
 * @returns 
 */
let suma = (a,b) => a + b;

let resultado = suma(10,5);

alert(resultado);